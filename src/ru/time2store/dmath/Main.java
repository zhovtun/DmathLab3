package ru.time2store.dmath;

import java.util.*;

public class Main
{


   public void printArray (String[] array) {
        System.out.print("[ ");
        int i = 0;
        while (i< array.length) {
            System.out.print(array[i] + " ");
            i++;
        }
        System.out.print("] \n");
    }











}